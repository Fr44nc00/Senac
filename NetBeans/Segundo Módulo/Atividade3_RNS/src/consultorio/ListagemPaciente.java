    public void atualizarListaPacientes() {

        if (!listaPacientes.isEmpty()) {

            Paciente pac;
            
            tableModel = new DefaultTableModel(tableColumns, 0);

            for (int i = 0; i < listaPacientes.size(); i++) {
                pac = listaPacientes.get(i);

                /*
               // TA DANDO ERRADO AQUI (CheckBox Já é Paciente)
               String lblPaciente = "Não";
              
               if (pac.getIsPaciente()) {
               lblPaciente = "Sim";
               }
               
               String lblConsulta = pac.getConsultaRealizada()? "Sim" : "Não";
               // TA DANDO ERRADO AQUI (CheckBox Já é Paciente)
                 */
                String[] linha = {pac.getNome(), pac.getCpf(), pac.getTelefone(), pac.getDataConsulta(), /*lblPaciente, lblConsulta*/};
                tableModeI.addRow(linha);
            }
            tblPacientes.setModel(tableModeI);

        } else {
            tableModel = new DefaultTableModel(tableColumns, 0);
            tblPacientes.setModel(tableModeI);
        }
    }

