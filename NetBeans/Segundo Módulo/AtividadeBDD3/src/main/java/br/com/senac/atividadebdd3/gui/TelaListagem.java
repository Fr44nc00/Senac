    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try{
            if(tblPodcast.getSelectedRow() >= 0){//Verifica se há algo selecionado na
                //tabela
                //Obtém o valor da coluna id da linha selecionada
                String id = (String)tblPodcast.getValueAt(tblPodcast.getSelectedRow(), 0);
                //Janela de confirmação
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o"
                        + " registro " + id + "?");
                if(resposta == 0)//0- yes, 1- no, 2- cancel
                {   
                    //Realizando a exclusão
                    dao.excluir(Integer.parseInt(id));
                    JOptionPane. showMessageDialog(this, "Registro excluído com sucesso");
                    //Refazendo a pesquisa para atualizar a tabela na tela
                    List<Podcast> podcast = dao.listar1();
                    preencherTabela(podcast);
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

