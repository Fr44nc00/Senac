    //Função para determinar o tipo de usuário
    private void tipoUsuario() {
        if(usuarioAtual.getTipo().equalsIgnoreCase("Admin") ) {
            btnExcluir.setEnabled(true);
        } else if (usuarioAtual.getTipo().equalsIgnoreCase("Oper") ) {
            btnExcluir.setEnabled(false);
        } else if (usuarioAtual.getTipo().equalsIgnoreCase("Usuario")){
            btnExcluir.setEnabled(false);
        }
    }
    
    //Função para preencher a tabela
    public void preencherTabela(List<Podcast> podcast){      
        String columns[] = {"Id", "Produtor", "Nome do Episódio","Número do Episódio",
            "Duração (em minutos)", "URL do Repositório"};
        String dados[][] = new String[podcast.size()][columns.length];

        int i=0;
        for(Podcast p: podcast){
            dados[i] = new String[]{ 
                String.valueOf(p.getId()),
                p.getProdutor(),
                p.getNomeEpisodio(),
                String.valueOf(p.getNumEpisodio()),
                p.getDuracao(),
                p.getRepositorio()};
            i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, columns);
        tblPodcast.setModel(model);
    }

