    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Podcast podcast = new Podcast();
        
        try{
            //Recebe os dados inseridos e passa eles para as variáveis
            podcast.setProdutor(txtProdutor.getText());
            podcast.setNomeEpisodio(txtNomeEpisodio.getText());
            podcast.setNumEpisodio(Integer.parseInt(txtNumEpisodio.getText()));
            podcast.setDuracao(txtDuracao.getText());
            podcast.setRepositorio(txtRepositorio.getText());

            PodcastDAO dao = new PodcastDAO();
            dao.cadastrar(podcast);
            
            JOptionPane.showMessageDialog(null,"Podcast cadastrado com sucesso.");
            //Limpa os campos
            txtProdutor.setText("");
            txtNomeEpisodio.setText("");
            txtNumEpisodio.setText("");
            txtDuracao.setText("");
            txtRepositorio.setText("");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

