    public void atualizarTabela(){
     
        if(!cadastrosList.isEmpty()){
         
            Cadastros cadastro;
         
            for (int i = 0; i < cadastrosList.size(); i++) {
             
                cadastro = cadastrosList.get(i);
             
                String[] rowData = {cadastro.getNome(), cadastro.getCPF(), cadastro.getTelefone(), cadastro.getData(), cadastro.getEraPaciente(), cadastro.getConsReal()};
             
                tableModel.addRow(rowData);
            }
         
            tblConsultas.setModel(tableModel);
        }else{
            tableModel = new DefaultTableModel(tableColumns, 0);
            tblConsultas.setModel(tableModel);
        }     
    }
    
