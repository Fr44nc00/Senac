    private void excluirConsulta(){
     
        int posConsulta = tblConsultas.getSelectedRow();
        
        if(posConsulta >= 0){
        
            String[] options = { "Sim", "Não" };
         
            int deletar = JOptionPane.showOptionDialog(
                    rootPane,
                    "Tem certeza que deseja excluir?", 
                    "Exclusão de Consulta", 
                    JOptionPane.DEFAULT_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, 
                    null, 
                    options, 
                    options[0]
            );
         
            if(deletar == 0){
                DefaultTableModel tabela = (DefaultTableModel) tblConsultas.getModel();
                tabela.removeRow(posConsulta);    
                cadastrosList.remove(posConsulta);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma consulta.");
        }
    }  
