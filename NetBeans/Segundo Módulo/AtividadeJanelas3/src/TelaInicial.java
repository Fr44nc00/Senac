    private void FinalizarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinalizarCActionPerformed
        
        int posConsulta = tblConsultas.getSelectedRow();
        
        if (posConsulta != -1){
            Cadastros cadastroSelect = cadastrosList.get(posConsulta);
            TelaFinalizacao tFinal = new TelaFinalizacao(cadastroSelect);
            tFinal.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma consulta", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_FinalizarCActionPerformed

