   private void ListagemTabela(List<Object[]> p) {
        String colunas[] = {"Produto", "Nome", "Marca", "PreÃ§o Und","Qtd Estoque", "Fornecedor"};
        String dados[][] = new String[p.size()][colunas.length];

        int i = 0;
        for (Object[] row : p) {
            int idProduto = (int) row[0];
            String nomeProduto = (String) row[1];
            String marcaProduto = (String) row[2];
            BigDecimal precoProduto = (BigDecimal) row[3];
            int quantidadeProduto = (int) row[4];
            String nomeFornecedor = (String) row[5];

            dados[i][0] = String.valueOf(idProduto);
            dados[i][1] = nomeProduto;
            dados[i][2] = marcaProduto;
            dados[i][3] = String.valueOf(precoProduto);
            dados[i][4] = String.valueOf(quantidadeProduto);
            dados[i][5] = nomeFornecedor;

            i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblProdutos.setModel(model);
    }
   
    private List<Object> passaObjeto() {
            int coluna = tblProdutos.getColumnCount();
            List<Object> rowData = new ArrayList<>();

            for (int i = 0; i < coluna; i++) {
                Object cellValue = tblProdutos.getValueAt(tblProdutos.getSelectedRow(), i);
                rowData.add(cellValue);
            }
            return rowData;
    }

    private List<Object[]> PesquisaListagem() {
        FornecedorProdutoDAO fpDao = new FornecedorProdutoDAO();
        String nome = "", id = "", fornecedor = "";
        try {
            int i = 0;
            if (txtNome.getText() != null && !txtNome.getText().isEmpty()) {
                nome = txtNome.getText();
                i = i + 1;
            }
            if (txtId.getText() != null && !txtId.getText().isEmpty()) {
                id = txtId.getText();
                i = i + 3;
            }
            if (txtFornecedor.getText() != null && !txtFornecedor.getText().isEmpty()) {
                fornecedor = txtFornecedor.getText();
                i = i + 5;
            }

            switch (i) {

                case 1 -> //nome
                {
                    List<Object[]> p = fpDao.FiltrarProdutos(nome);
                    return p;
                }
                case 3 -> //id
                {
                    List<Object[]> p = fpDao.FiltrarProdutosId(id);
                    return p;
                }
                case 4 -> // nome e id
                {
                    List<Object[]> p = fpDao.FiltrarProdutos(nome, id);
                    return p;
                }
                case 5 -> //fornecedor
                {
                    List<Object[]> p = fpDao.filtrarFornecedor(fornecedor);
                    return p;
                }
                case 6 -> //fornecedor e nome
                {
                    List<Object[]> p = fpDao.filtrarFornecedorENome(fornecedor, nome);
                    return p;
                }
                case 8 -> //fornecedor e id
                {
                    List<Object[]> p = fpDao.filtrarFornecedorEId(fornecedor, id);
                    return p;
                }
                case 9 -> //Pesquisa todos
                {
                    List<Object[]> p = fpDao.FiltrarProdutos(nome, id, fornecedor);
                    return p;
                }
                case 0 ->
                {
                   JOptionPane.showMessageDialog(rootPane,"Preencha um dos campos para filtrar.");
                   List<Object[]> p = fpDao.ListarProdutos();
                    return p; 
                }    
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Fornecedor fornecido nÃ£o valido.");
            return null;
        }
        return null;

    }

