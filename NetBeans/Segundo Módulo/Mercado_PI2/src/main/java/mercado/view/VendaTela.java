private void ListagemTabela(List<Object[]> p) {
        DefaultTableModel model = (DefaultTableModel) tblProdutos.getModel();
        
        this.x = this.x + 1;
        
        int i = 0;
        
        for (Object[] row : p) {
            
            int idProduto = (int) row[0];
            String nomeProduto = (String) row[1];
            String marcaProduto = (String) row[2];
            int quantidadeProduto = Integer.parseInt(txtQtd.getText()); //quantidade selecionada pelo vendedor
            BigDecimal precoProduto = (BigDecimal) row[3];
            String Descricao = (String) row[4];

        String[] novaLinha = {
        String.valueOf(this.x),
        String.valueOf(idProduto),
        nomeProduto,
        marcaProduto,
        String.valueOf(quantidadeProduto),
        String.valueOf(precoProduto)      
    };
        if(txtDescricao.getText().equals("Descrição Produto:")){
        txtDescricao.setText(Descricao);
        }else if(txtDescricao.getText().equals("Anotações encomenda:")){
        txtDescricao.setText("");    
        }
        model.addRow(novaLinha);
        tblProdutos.setModel(model);
        
        AtualizaDados(precoProduto,nomeProduto, quantidadeProduto);
        
        i++;
        }

       
    }

public void AtualizaDados(BigDecimal valor, String nome, int qtd) {
    // Converte a quantidade para BigDecimal
    BigDecimal qtdBig = new BigDecimal(qtd);
   
    // Calcula o total do produto atual
    BigDecimal totalP = valor.multiply(qtdBig);
    
    // Soma o total do produto atual ao txtTotalLiquido
    BigDecimal totalLiquido = new BigDecimal(txtTotal.getText()).add(totalP);
    
    // Exibe o nome e preço do produto
    txtNomeProduto.setText(nome);
    txtPrecoProduto.setText("R$ " + valor.toString());
    
    // Atualiza o txtTotalLiquido com o novo total líquido
    txtTotal.setText(totalLiquido.toString());
}

