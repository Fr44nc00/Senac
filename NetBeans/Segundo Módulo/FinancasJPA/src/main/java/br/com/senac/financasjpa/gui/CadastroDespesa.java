    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Despesa novaDespesa = new Despesa();
        if(despesaEdicao != null){
          novaDespesa = despesaEdicao;
        }
        
        try{
            //para a descrição capturamos direto o valor do campo de texto
            novaDespesa.setDescricao(txtDescricao.getText());
            //substitui , por . para que fique no formato numérico que o Java
            //entende como double
            novaDespesa.setValor(Double.parseDouble(fmtTxtValor.getText().replace(",", ".")));
            //classe DateFormatter necessária para obter o tipo LocalDate a partir
            //de um texto
            novaDespesa.setData(LocalDate.parse(fmtTxtData.getText(), DateTimeFormatter.
                    ofPattern("dd/MM/y")));

            DespesaDAO despesaDao = new DespesaDAO();
            if(despesaEdicao == null){
                despesaDao.cadastrar(novaDespesa);
            }else{
                despesaDao.atualizar(novaDespesa);
            }
            
            //fechando a tela após o cadastro
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
        
        //O código mostra a montagem de um objeto da classe Despesa, no qual se destaca a
        //necessidade de ajuste no numeral obtido pelo campo fmtTxtValor. A formatação desse
        //campo resulta em um numeral separado das casas decimais por vírgula; daí a
        //necessidade de trocar a vírgula por ponto, pois Java entende numéricos com casas
        //decimais apenas separadas por ponto. Também se destaca a necessidade de formatação
        //de data: LocalDate.parse() obtém o valor de data a partir de um texto; porém, é
        //preciso informar ainda um padrão que esse método tem que obedecer ao analisar esse
        //texto. Por isso, utiliza-se DateTimeFormatter.ofPattern(), passando por parâmetro
        //o formato de data esperado (dia com dois dígitos/mês com dois dígitos/ano com
        ///quatro dígitos). A persistência em si acontece com a instanciação do objeto
        //despesaDao e a invocação do método cadastrar(), que foram implementados
        //anteriormente.
    }//GEN-LAST:event_btnSalvarActionPerformed

